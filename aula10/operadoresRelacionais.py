# == > < >= <= !=
# = afirmando se uma coisa é igual a outra, == perguntando se uma coisa é igual a outra

print("OPERADOR ==")
#num_1 = 2
#num_2 = '2'
#print(num_1==num_2)
num_3 = 1
num_4 = 1
#print(num_3==num_4)
expressao = (num_3 == num_4)
print(expressao) #True

print("OPERADOR >")
#MAIOR QUE
num_5 = 5
num_6 = 1
expressao1 = (num_5 > num_6)
print(expressao1) #True

print("OPERADOR <")
#MENOR QUE
num_7 = 5
num_8 = 1
expressao2 = (num_7 < num_8)
print(expressao2) #False

print("OPERADOR <=")
#MENOR OU IGUAL A
num_9 = 5
num_10 = 5
expressao3 = (num_9 <= num_10)
print(expressao3) #True

print("OPERADOR >=")
#MAIOR OU IGUAL A
num_11 = 1
num_12 = 8
expressao4 = (num_11 >= num_12)
print(expressao4) #False

print("OPERADOR !=")
#DIFERENTE DE
num_13 = 1
num_14 = 8
expressao5 = (num_13 != num_14)
print(expressao5) #True